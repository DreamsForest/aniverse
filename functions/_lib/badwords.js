// ============================================================
//  Фильтр запрещённых слов для комментариев.
//  Цель — не пропускать мат, оскорбления и унижения.
//  Проверка ВСЕГДА на сервере: клиент её обойти не может.
//
//  Как работает:
//   1) текст нормализуется (нижний регистр, латинские/цифровые
//      «двойники» букв → кириллица, убираются разделители между
//      буквами — ловим «с у к а», «c_y_к@» и т.п.);
//   2) ищем корни из списка ROOTS как подстроки в нормализованном
//      тексте, при этом исключаем «безопасные» слова из ALLOW.
//
//  Список заведомо неполный и эвристический (возможны ложные
//  срабатывания) — его легко дополнять.
// ============================================================

// Латинские и цифровые двойники кириллицы → кириллица.
const HOMOGLYPHS = {
  a: "а", b: "в", c: "с", e: "е", h: "н", k: "к", m: "м", o: "о",
  p: "р", t: "т", x: "х", y: "у", u: "и", n: "п", r: "г",
  "0": "о", "3": "з", "4": "ч", "6": "б", "@": "а", $: "с", "!": "и",
};

// Нормализация: режем регистр, меняем двойников, схлопываем повторы,
// и формируем «склеенную» версию без разделителей между одиночными буквами.
export function normalize(text) {
  let s = String(text || "").toLowerCase();
  // ё → е
  s = s.replace(/ё/g, "е");
  // двойники
  s = s.replace(/[a-z0-9@$!]/g, (ch) => HOMOGLYPHS[ch] || ch);
  return s;
}

// «Склеенная» форма: оставляем только буквы (рус/лат), убираем всё
// прочее. Ловит «с.у.к.а», «с у к а», «с-у-к-а».
function glued(normalized) {
  return normalized.replace(/[^а-яa-z]/g, "");
}

