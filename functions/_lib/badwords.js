// Безопасные слова, которые случайно содержат «опасные» корни.
// Если в тексте есть только эти слова из подозрительных — не блокируем.
const ALLOW = [
  "страх", "психуй", "ночуй", "кочуй", "район", "герой", "лохань",
  "хербарий", "херес", "гейзер", "гейм", "гейша", "регей", "регги",
  "скотч", "скотовод", "очков", "очки", "сочко", "трахея",
  "достоевск", "соскуч", "наскуч", "прискуч",
  "психотерап", "психолог", "психиатр",
];

// Возвращает { blocked: bool, word: string|null }.
export function checkBadWords(text) {
  const norm = normalize(text);
  const g = glued(norm);

  // снимаем «безопасные» слова из склеенной формы, чтобы они не давали ложных хитов
  let probe = g;
  for (const safe of ALLOW) {
    const sg = glued(normalize(safe));
    if (sg) probe = probe.split(sg).join(" ");
  }

  for (const root of ROOTS) {
    const r = glued(normalize(root));
    if (r && probe.includes(r)) {
      return { blocked: true, word: root.trim() };
    }
  }
  return { blocked: false, word: null };
}
